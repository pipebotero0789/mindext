                            <p align="justify">San Petersburgo es la segunda ciudad más poblada de Rusia, con 5 026 000 habitantes (2013) y un área metropolitana de 5,85 millones. Está situada en la Región de Leningrado, nombre que compartía con la ciudad durante la época soviética (1924-1991). Los otros nombres de la ciudad fueron Petrogrado(Петрогра́д, del 31 de agosto de 1914 hasta el 24 de enero de 1924) y Leningrado (Ленингра́д, después de la muerte deLenin, el 24 de enero de 1924 hasta el 8 de septiembre de 1991).</p>

                            <p align="justify">Fue fundada por el zar Pedro el Grande el 16 de mayo de 1703 con la intención de convertirla en la "ventana de Rusia hacia el mundo occidental".2 A partir de entonces se convirtió en capital del Imperio ruso durante más de doscientos años. Con el estallido de la Revolución rusa, la ciudad se convirtió en el centro de la rebelión. En marzo de 1918 la capital fue trasladada a Moscú. En enero de 1924, tras la victoria bolchevique, la creación de la Unión Soviética (1922) y el fallecimiento de Lenin (1924), San Petersurgo cambió su nombre a "Leningrado", en honor al dirigente comunista Lenin. Durante la Segunda Guerra Mundial, tuvo lugar el sitio de Leningrado, que se mantuvo por 29 meses, en los cuales los nazis bombardearon constantemente la ciudad y la bloquearon para que no pudiera abastecerse. Tras la derrota de Alemania, en 1945, la ciudad fue nombrada Ciudad heroica por las autoridades soviéticas. Con la disolución de la Unión Soviética y el colapso del comunismo, la ciudad fue renombrada "San Petersburgo" y se ha convertido en un importante centro económico y político de la actual Rusia.</p>

                            <p align="justify">Actualmente San Petersburgo es la segunda ciudad más grande de la Federación Rusa y una de las ciudades más grandes de Europa. El centro de la ciudad es considerado patrimonio de la humanidad por la Unesco. San Petersburgo es además sede de la corte constitucional de Rusia.</p>
