                            <p align="justify">Samara (en ruso: Сама́ра), conocida como Kúibyshev desde 1935 hasta 1991 (en honor a Valerián Kúibyshev, en ruso:Ку́йбышев), es la sexta ciudad más grande de Rusia. Está situada en el río Volga, cerca de las Montes Zhigulí del Volga, en la zona sureste de la Rusia europea, en el Distrito Federal del Volga, centro administrativo del óblast de Samara. Su población es de 1.157.880 habitantes (según el censo ruso de 2002).</p>

