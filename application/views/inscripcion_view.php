      <h2>Preparatoria</h2>
      <p style="text-align:justify">La Preparatoria es la primera etapa de la estadía en Rusia, es 100% académica. El objetivo de esta primera fase es que el estudiante aprenda el idioma ruso y se adapte a vivir en este país. El estudiante cursará además entre 4 y 6 materias aproximadamente y al final de ellas tendrá que aprobar diferentes exámenes que afirmen que está listo para empezar su carrera universitaria.</p>
      <p style="text-align:justify">La duración de la preparatoria varía según la fecha de viaje:</p>
      <ul>
      	<li style="text-align:justify">Si el estudiante viaja en septiembre, esta tendrá una duración de 10 meses, culminando el 30 de junio del año siguiente. </li>
        <li style="text-align:justify">Si el estudiante viaja en febrero o marzo, la preparatoria tendrá una duración de 14 meses, con un periodo vacacional de  2 meses en el verano europeo (julio y agosto), culminando el 30 de junio del año siguiente.</li>
